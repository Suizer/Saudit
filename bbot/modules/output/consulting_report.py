FINDING_BLOCK = """
<div class="finding">
  <div class="finding-header" style="border-left-color:{color}">
    <span class="badge" style="background:{color}">{severity}</span>
    <span class="title">{title}</span>
    <span class="source-tag">{source}</span>
  </div>
  <div class="finding-body" style="background:{bg}">
    <table>{rows}</table>
  </div>
</div>"""


def _badge_color(sev: str) -> str:
    return SEVERITY_COLORS.get(sev, SEVERITY_COLORS["unknown"])


def _bg_color(sev: str) -> str:
    return SEVERITY_BG.get(sev, SEVERITY_BG["unknown"])


def _row(label: str, value: str) -> str:
    if not value:
        return ""
    return f"<tr><td>{label}</td><td><code>{value}</code></td></tr>"


def _render_finding(f: dict, source: str) -> str:
    sev = _sev(f)
    color = _badge_color(sev)
    bg = _bg_color(sev)
    title = f.get("title") or f.get("description", "Finding")[:120]
    rows = (
        _row("URL", f.get("url", ""))
        + _row("Endpoint", f.get("endpoint", ""))
        + _row("Type", f.get("type", ""))
        + _row("Description", f.get("description", ""))
        + _row("Evidence", (f.get("evidence") or f.get("match") or "")[:400])
        + _row("Context", (f.get("context") or f.get("recommendation") or "")[:400])
    )
    return FINDING_BLOCK.format(
        color=color, bg=bg, severity=sev.upper(),
        title=title, source=source, rows=rows
    )


def _section(section_id: str, heading: str, findings: list, source_tag: str) -> str:
    if not findings:
        content = '<p class="empty">No findings in this category.</p>'
    else:
        content = "\n".join(_render_finding(f, source_tag) for f in _sort_findings(findings))
    return f'<div class="section" id="{section_id}"><h2>{heading}</h2>{content}</div>'


class consulting_report(BaseOutputModule):
    watched_events = ["FINDING", "TECHNOLOGY", "VULNERABILITY"]
    meta = {
        "description": "Generate a unified HTML consulting report from BBOT, JsFuzzer, and MendixRecon findings",
        "created_date": "2024-01-01",
        "author": "@consulting",
    }
    options = {"output_file": ""}
    options_desc = {"output_file": "HTML report output path (default: <scan_dir>/CONSULTING_REPORT.html)"}

    output_filename = "CONSULTING_REPORT.html"

    async def setup(self):
        self._prep_output_dir(self.output_filename)
        self._findings = defaultdict(list)
        self._technologies = set()
        self._target = str(self.scan.target)
        return True

    async def handle_event(self, event):
        if event.type == "TECHNOLOGY":
            data = event.data
            if isinstance(data, dict):
                self._technologies.add(data.get("technology", ""))
            return

        tags = set(getattr(event, "tags", []) or [])
        data = event.data

        if not isinstance(data, dict):
            return

        finding = {
            "severity": "info",
            "title": "",
            "url": "",
            "endpoint": "",
            "type": event.type,
            "description": data.get("description", ""),
            "evidence": "",
            "context": "",
            "recommendation": "",
        }

        if event.type == "VULNERABILITY":
            finding["severity"] = data.get("severity", "medium").lower()
            finding["title"] = data.get("name", data.get("description", "Vulnerability"))[:200]
            finding["url"] = data.get("url", "")
        else:
            finding["title"] = data.get("description", "Finding")[:200]
            finding["url"] = data.get("url", "")

        # Classify by source based on tags
        if "jsfuzzer" in tags:
            bucket = "jsfuzzer"
        elif "mendix-recon" in tags:
            bucket = "mendix"
        else:
            bucket = "bbot"

        # Extract severity from tags if not in data
        for tag in tags:
            if tag.startswith("severity-"):
                finding["severity"] = tag.split("severity-", 1)[1]
                break

        self._findings[bucket].append(finding)

    async def report(self):
        all_findings = (
            self._findings["bbot"]
            + self._findings["jsfuzzer"]
            + self._findings["mendix"]
        )

        # Count by severity across all buckets
        counts = defaultdict(int)
        for f in all_findings:
            counts[_sev(f)] += 1

        # Summary cards
        summary_cards = "\n".join(
            SUMMARY_CARD.format(
                color=_badge_color(sev),
                count=counts.get(sev, 0),
                label=sev.capitalize(),
            )
            for sev in ["critical", "high", "medium", "low", "info"]
        )
        summary_cards += SUMMARY_CARD.format(
            color="#7f8c8d",
            count=len(self._technologies),
            label="Technologies",
        )
        summary_html = f'<div class="summary-grid">{summary_cards}</div>'

        # TOC
        sections = [
            ("bbot-findings", "BBOT Native Findings"),
            ("js-findings", "JavaScript Analysis (JsFuzzer)"),
            ("mendix-findings", "Mendix Audit (MendixRecon)"),
        ]
        toc_html = " ".join(f'<a href="#{sid}">{name}</a>' for sid, name in sections)

        # Sections
        sections_html = "\n".join([
            _section("bbot-findings", "BBOT Native Findings", self._findings["bbot"], "bbot"),
            _section("js-findings", "JavaScript Analysis (JsFuzzer)", self._findings["jsfuzzer"], "jsfuzzer"),
            _section("mendix-findings", "Mendix Audit (MendixRecon)", self._findings["mendix"], "mendix-recon"),
        ])

        html = HTML_TEMPLATE.format(
            target=self._target,
            scan_date=datetime.now().strftime("%Y-%m-%d %H:%M UTC"),
            sev_color=_badge_color("critical"),
            sev_bg=_bg_color("info"),
            summary_html=summary_html,
            toc_html=toc_html,
            sections_html=sections_html,
        )

        with suppress(Exception):
            self.output_file.write_text(html, encoding="utf-8")
            self.info(f"Consulting report saved to {self.output_file}")

    async def cleanup(self):
        with suppress(Exception):
            if getattr(self, "_file", None) is not None:
                self._file.close()
