SEVERITY_ORDER = {"critical": 0, "high": 1, "medium": 2, "low": 3, "info": 4, "unknown": 5}

SEVERITY_COLORS = {
    "critical": "#c0392b",
    "high":     "#e67e22",
    "medium":   "#f1c40f",
    "low":      "#27ae60",
    "info":     "#2980b9",
    "unknown":  "#7f8c8d",
}

SEVERITY_BG = {
    "critical": "#fde8e8",
    "high":     "#fef0e6",
    "medium":   "#fefde6",
    "low":      "#e8f8ee",
    "info":     "#e6f2fb",
    "unknown":  "#f0f0f0",
}


def _sev(finding: dict) -> str:
    return finding.get("severity", "unknown").lower()


def _sort_findings(findings: list) -> list:
    return sorted(findings, key=lambda f: SEVERITY_ORDER.get(_sev(f), 5))


